#!/usr/bin/env node
/**
 * color-pairings.js — Color Pairing builder, Figma-variables-only.
 *
 * Reads:
 *   - Figma variables in the `semantic` collection (prefix `ob/s/color/`)
 *   - `registry.json` (layout rules + section/group text)
 *
 * Writes (depending on --mode):
 *   - Figma page "🎨  Colors – Contrast Pairings cli" (mode: all | figma)
 *   - src/lib/contrast-pairings.json                  (mode: all | export)
 *   - src/lib/accessible-pairings.md                  (mode: all | export)
 *
 * Usage:
 *   node color-pairings.js                       # build everything (Figma + JSON + MD + validate)
 *   node color-pairings.js --mode figma          # Figma docs only + validate
 *   node color-pairings.js --mode export         # JSON + MD only (no Figma writes)
 *   node color-pairings.js --mode validate       # read existing page, run checks, no writes
 *   node color-pairings.js --category neutral    # restrict to one category (for iteration)
 *
 * Validation:
 *   Every build-touching mode (all, figma) runs validatePage() at the end.
 *   --mode validate runs only the checks.
 *   Any validation error → exit code 1. Warnings → printed, exit 0.
 */
