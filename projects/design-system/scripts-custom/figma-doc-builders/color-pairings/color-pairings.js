// ── figma-ds-cli wrapper ───────────────────────────────────────────────────
function runEval(scriptText) {
  const tmp = path.join(os.tmpdir(), `cp-build-${process.pid}-${Date.now()}.js`);
  fs.writeFileSync(tmp, scriptText);
  try {
    const res = spawnSync(CLI, ['run', tmp], {
      encoding: 'utf8',
      maxBuffer: 64 * 1024 * 1024,
      cwd: FIG_CLI_DIR,
      env: { ...process.env, FIG_EVAL_TIMEOUT_MS: process.env.FIG_EVAL_TIMEOUT_MS }
    });
    if (res.error) throw res.error;
    return { status: res.status, stdout: res.stdout || '', stderr: res.stderr || '' };
  } finally {
    try { fs.unlinkSync(tmp); } catch {}
  }
}

function extractResult(stdout) {
  const i = stdout.search(/[{[]/);
  if (i < 0) return null;
  for (let end = stdout.length; end > i; end--) {
    try { return JSON.parse(stdout.slice(i, end)); } catch {}
  }
  return null;
}

// ── Markdown renderer (used after the eval returns) ───────────────────────
function renderMd(out) {
  const yn = b => (b ? '✓' : '✗');
  const lines = [];
  lines.push('# Accessible Color Pairings — Oblique Design System');
  lines.push('');
  lines.push('> Auto-generated by `scripts-custom/figma-doc-builders/color-pairings/color-pairings.js`. Do not edit by hand.');
  lines.push('> Source: Figma variables (`ob/s/color/*`) + `registry.json`.');
  lines.push('> Generated: ' + out.generatedAt);
  lines.push('');
  for (const [cat, swatches] of Object.entries(out.categories)) {
    lines.push('## ' + cat[0].toUpperCase() + cat.slice(1) + ' (' + swatches.length + ' pairings)');
    lines.push('');
    if (!swatches.length) { lines.push('_no swatches_'); lines.push(''); continue; }
    lines.push('| Foreground | Background | Ratio | AA‑N | AA‑L | AAA | Spec | Emph |');
    lines.push('|---|---|---:|:-:|:-:|:-:|---|---|');
    for (const s of swatches) {
      if (s.missing) {
        lines.push('| `' + (s.fgName || '?') + '` | `' + (s.bgName || '?') + '` | — | — | — | — | _missing_ | |');
        continue;
      }
      if (s.isSingle) {
        lines.push('| `' + s.fg + '` | — | — | — | — | — | _single token, no pairing_ | |');
        continue;
      }
      const w = s.wcag || {};
      const r = (s.ratio != null) ? s.ratio.toFixed(1) + ' : 1' : '—';
      lines.push('| `' + s.fg + '` | `' + s.bg + '` | ' + r + ' | ' + yn(w.aaNormal) + ' | ' + yn(w.aaLarge) + ' | ' + yn(w.aaa) + ' | ' + (s.cs || '') + ' | ' + (s.emph ? '⚠ ' + s.emph : '') + ' |');
    }
    lines.push('');
  }
  return lines.join('\n');
}

